import re

def map_molecular_profile_to_genes(profiles):
    gene_pattern = re.compile(r'^([A-Z0-9]+(?:-[A-Z0-9]+)*)')
    result = {}
    for profile in profiles:
        genes = set()
        
        parts = re.split(r'\s+(?:AND|OR)\s+', profile)
        for part in parts:
            part = part.strip()
            if '::' in part:
                
                for gene_str in part.split('::'):
                    m = gene_pattern.match(gene_str)
                    if m:
                        genes.add(m.group(1))
            else:
                
                m = gene_pattern.match(part)
                if m:
                    genes.add(m.group(1))

        if not genes:
            
            pass
        if len(genes) > 1:
            
            pass
        result[profile] = ";".join(sorted(genes))
    if 'Kataegis Positive' in result.keys():
        result['Kataegis Positive'] = '' 
    return result


def compare_two_mappings(mapping1, mapping2):
    assert np.unique(list(mapping1.keys())).shape[0]==len(list(mapping1.keys()))
    assert all(np.unique(list(mapping1.keys()))==np.unique(list(mapping2.keys())))
    def normalize(value: str) -> set[str]:
        
        return {part.strip() for part in value.split(';') if part.strip()}
    for k, v in mapping1.items():
        try:
            a = normalize(v)
            b = normalize(mapping2[k])
            if a != b:
                print(f"Mismatch for {k}: {a} != {b}")
        except:
            print("BUGGED for key:", k)

